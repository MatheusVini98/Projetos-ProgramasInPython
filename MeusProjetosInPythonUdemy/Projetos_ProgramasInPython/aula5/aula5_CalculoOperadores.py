"""
+ # adição ,-# subtração ,* # multiplicação ,/ # divisão ,// #Divisão inteira ,** # Exponenciação ,% Retorna o Módulo
, () #Ordem de procedência.
"""
print('Adição + ', 10 + 10)
print('Subtração -', 10 - 5)
print('Multiplicação *', 10 * 10)
print('Divisão /', 10 / 10)
print('Divisão Inteira //', 10 // 10)
print('Potenciação **', 10 ** 10)
print('Módulo da Divisão(resto da divisão) %', 10 % 10)
print('Ordem de precedência()', (5+2)*10)


print('Multiplicação de Concatenar', 10 * '10')
print('Adição de Concatenar', '10' + '10')

